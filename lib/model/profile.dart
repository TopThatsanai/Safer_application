class Profile {
  String username;
  String password;
  String confirmPassword;
  String firstName;
  String lastName;
  String citizenId;
  String email;
  String phoneNumber;

  Profile({
    required this.username,
    required this.password,
    required this.confirmPassword,
    required this.firstName,
    required this.lastName,
    required this.citizenId,
    required this.email,
    required this.phoneNumber,
  });
}
